void printMatrix(uint32 m[])
{
    for (int i = 0; i < 4; i++)
    {
        for (int j = 0; j < 4; j++)
            printf("%08x\t", m[4*i+j]);
        printf("\n");
    }
}
int compare(const byte* expected, const byte* actual, size_t l)
{
    for (size_t i = 0; i < l; i++)
    {
        if(actual[i] != expected[i])
        {
            printf("Position %i: expected value %02x but got %02x\n", i, expected[i], actual[i]);
            return 1;
        }
    }
    return 0;
}
